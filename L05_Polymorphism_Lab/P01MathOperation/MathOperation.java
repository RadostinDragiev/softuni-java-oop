package L05_Polymorphism_Lab.P01MathOperation;

public class MathOperation {
    public Integer add(int a, int b) {
        return a + b;
    }

    public Integer add(int a, int b, int c) {
        return a + b + c;
    }

    public Integer add(int a, int b, int c, int d) {
        return a + b + c + d;
    }
}
